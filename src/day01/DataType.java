package day01;

public class DataType {
	public static void main (String args[]) {
		byte b =124;
		System.out.println("the value is byte:"+ b);
		int a =(5*12);
		System.out.println("the value int is:"+a);
		char c ='n';
		System.out.println("the value char is:"+c);
		short s =(15+23)*(150-654);
		System.out.println("the value short is:"+s);
		long l=(12412l*266);
		System.out.println("the value long is:"+l);
		float f = 0.2564f;
		System.out.println("the value float is:"+f);
		double d =15453/32456d;
		System.out.println("the value double  is:"+d);
		String name="nithih";
		System.out.println("the value string is:"+ name);
		boolean boo = false;
		System.out.println("the value of boolean  is:"+boo);
		
	}
	

}
