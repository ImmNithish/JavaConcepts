package day01;

public class TypeConversion {
	public static void main(String args[]) {
		byte b =120;
		int i= b;
		System.out.println(" the implicit value is :"+i);
		
		float f=((15*36)-(15/4));
		int i1= (int) f;
		System.out.println(" explicit value is:"+ i1);
		double d= 0.244515d;
		int a= (int)d;
		System.out.println(" explicit value is :"+a);
	}

}





